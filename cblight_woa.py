# =========================
# 3) BANGUN CNN–BiLSTM (Conv1D k=3 f=64 ReLU, BiLSTM 128)
#    Output softmax hanya untuk melatih feature extractor
# =========================
text_in = Input(shape=(timesteps, features), name="text_w2v")
x = layers.Conv1D(filters=64, kernel_size=3, padding="same", activation="relu")(text_in)
x = layers.Bidirectional(layers.LSTM(128, return_sequences=False))(x)
x = layers.Dense(64, activation="relu", name="text_embed")(x)  # embedding teks

num_in = Input(shape=(Xn_train.shape[1],), name="num_in")
n = layers.Dense(32, activation="relu")(num_in)

fusion = layers.Concatenate()([x, n])
fusion = layers.Dense(64, activation="relu", name="fusion_feat")(fusion)
out = layers.Dense(n_classes, activation="softmax")(fusion)

cnn_bilstm_model = Model(inputs=[text_in, num_in], outputs=out)
