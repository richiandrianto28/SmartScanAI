def tokenize(text: str):
    if pd.isna(text):
        return []
    s = str(text).lower()
    s = re.sub(r"[^a-z0-9\s]", " ", s)
    s = re.sub(r"\s+", " ", s).strip()
    return s.split()

# 5) BENTUK VEKTOR DOKUMEN (MEAN POOLING WORD2VEC)
# =========================
def doc_vector(tokens):
    valid = [wv[t] for t in tokens if t in wv.key_to_index]
    if len(valid) == 0:
        return np.zeros(vec_dim, dtype=np.float32)
    return np.mean(valid, axis=0).astype(np.float32)

